package com.example.shreehari.Model;

public class AssignmentModel {

    public  String class_work;
    public  String home_work;
    public  String date;
    public  String due_date;
    public  String standard;
    public  String batch;
    private String id;

    public String getClass_work() {
        return class_work;
    }

    public void setClass_work(String class_work) {
        this.class_work = class_work;
    }

    public String getHome_work() {
        return home_work;
    }

    public void setHome_work(String home_work) {
        this.home_work = home_work;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDue_date() {
        return due_date;
    }

    public void setDue_date(String due_date) {
        this.due_date = due_date;
    }

    public String getStandard() {
        return standard;
    }

    public void setStandard(String standard) {
        this.standard = standard;
    }

    public String getBatch() {
        return batch;
    }

    public void setBatch(String batch) {
        this.batch = batch;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }
}

