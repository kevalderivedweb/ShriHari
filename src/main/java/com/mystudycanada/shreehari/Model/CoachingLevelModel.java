package com.mystudycanada.shreehari.Model;

public class CoachingLevelModel {
    private String coachinglevel_id;
    private String coachinglevel;
    private String coaching_type_id;
    private String branch_id;
    private String status;

    public String getCoachinglevel_id() {
        return coachinglevel_id;
    }

    public void setCoachinglevel_id(String coachinglevel_id) {
        this.coachinglevel_id = coachinglevel_id;
    }

    public String getCoachinglevel() {
        return coachinglevel;
    }

    public void setCoachinglevel(String coachinglevel) {
        this.coachinglevel = coachinglevel;
    }

    public String getCoaching_type_id() {
        return coaching_type_id;
    }

    public void setCoaching_type_id(String coaching_type_id) {
        this.coaching_type_id = coaching_type_id;
    }

    public String getBranch_id() {
        return branch_id;
    }

    public void setBranch_id(String branch_id) {
        this.branch_id = branch_id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
