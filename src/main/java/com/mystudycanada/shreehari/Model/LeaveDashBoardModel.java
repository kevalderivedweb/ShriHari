package com.mystudycanada.shreehari.Model;

public class LeaveDashBoardModel {
    private String coachId;
    private String breakFrom;
    private String breakTo;
    private String days;
    private String status;
    private String userId;
    private String firstName;
    private String lastName;
    private String registerNumber;

    public void setCoachId(String coachId) {
        this.coachId = coachId;
    }

    public String getCoachId() {
        return coachId;
    }

    public void setBreakFrom(String breakFrom) {
        this.breakFrom = breakFrom;
    }

    public String getBreakFrom() {
        return breakFrom;
    }

    public void setBreakTo(String breakTo) {
        this.breakTo = breakTo;
    }

    public String getBreakTo() {
        return breakTo;
    }

    public void setDays(String days) {
        this.days = days;
    }

    public String getDays() {
        return days;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getStatus() {
        return status;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getUserId() {
        return userId;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setRegisterNumber(String registerNumber) {
        this.registerNumber = registerNumber;
    }

    public String getRegisterNumber() {
        return registerNumber;
    }
}
