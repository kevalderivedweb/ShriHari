package com.mystudycanada.shreehari.ui;

public class StandardModel {
    public String coaching_id;
    public String coaching;
    public String status;

    public String getCoaching_id() {
        return coaching_id;
    }

    public void setCoaching_id(String coaching_id) {
        this.coaching_id = coaching_id;
    }

    public String getCoaching() {
        return coaching;
    }

    public void setCoaching(String coaching) {
        this.coaching = coaching;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
